package ControlFlow.LoopsInJava.DoWhileLoop;

public class DoWhile_Intro {
  
  public static void main(String[] args) {
    
    
    int i  = 0;
    
    do{
	  System.out.println(i);
	  i++;
	}while (i < 0);
	
    
    int j = 4;
    
    do {
	  System.out.println(j);
	  j++;
	}while (j < 3);
  
  
  }
}
